class Person {
    constructor(fn, ln, m, a, nid, bd, cn, pass) {
        this.FirstName = fn
        this.LastName = ln
        this.Mail = m
        this.Address = a
        this.NID = nid
        this.BirthDate = bd
        this.ContactNo = cn
        this.Password = pass
    }
}
module.exports = Person;